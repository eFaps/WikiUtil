/**
 * Enumeration to define type faces.
 *
 * @author The eFaps Team
 * @version $Id$
 */
public enum ETypeface
{
    /** Bold. */
    BOLD,
    /** Code (courier). */
    CODE,
    /** Italic (cursive). */
    ITALIC,
    /** Strike out. */
    STRIKEOUT,
    /** Super script. */
    SUPER,
    /** Sub script. */
    SUB;
}
